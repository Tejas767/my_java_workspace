package com.library;

public class Libdetails {
	
	
	private long contact_number;
	private String librarian_name;
	private String location;
	private int total_books;
	
	
	

	public long getContact_number() {
		return contact_number;
	}
	public void setContact_number(long contact_number) {
		this.contact_number = contact_number;
	}
	public String getLibrarian_name() {
		return librarian_name;
	}
	public void setLibrarian_name(String librarian_name) {
		this.librarian_name = librarian_name;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public int getTotal_books() {
		return total_books;
	}
	public void setTotal_books(int total_books) {
		this.total_books = total_books;
	}
	
	
	
	
	
	
}
